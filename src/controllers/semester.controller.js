const Semester = require('../models/semester.model');

exports.readAll = (req, res) => {

    Semester.readAll((err, data) => {
        if (err) {
            res.status(500).send({
                status: "error",
                message: err.message
            });
        } else {    
            res.status(200).send({
                status: "success",
                data
            });
        }
    })
}